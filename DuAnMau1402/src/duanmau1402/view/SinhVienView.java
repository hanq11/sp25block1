    private void tblSinhVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSinhVienMouseClicked
        int selectedRow = tblSinhVien.getSelectedRow();
        SinhVien sv = sinhVienRepository.getAll().get(selectedRow);
        txtMaSinhVien.setText(String.valueOf(sv.getMaSinhVien()));
        txtTen.setText(sv.getTen());
        txtTuoi.setText(String.valueOf(sv.getTuoi()));
        txtNgaySinh.setText(sv.getNgaySinh());
        txtDiaChi.setText(sv.getDiaChi());
    }//GEN-LAST:event_tblSinhVienMouseClicked

