    public void fillTableData(ArrayList<SinhVien> danhSach) {
        DefaultTableModel dtm = (DefaultTableModel) tblSinhVien.getModel();
        dtm.setRowCount(0);
        for(SinhVien sv: danhSach) {
            Object[] thongTin = {
                sv.getMaSinhVien(),
                sv.getTen(),
                sv.getTuoi(),
                sv.getNgaySinh(),
                sv.getDiaChi()
            };
            dtm.addRow(thongTin);
        }
    }
    
    public SinhVien getFormData() {
        Integer maSinhVien = Integer.valueOf(txtMaSinhVien.getText());
        String ten = txtTen.getText();
        Integer tuoi = Integer.valueOf(txtTuoi.getText());
        String ngaySinh = txtNgaySinh.getText();
        String diaChi = txtDiaChi.getText();
        return new SinhVien(maSinhVien, ten, tuoi, ngaySinh, diaChi);
    }
